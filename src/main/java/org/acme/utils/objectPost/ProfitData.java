package org.acme.utils.objectPost;

public class ProfitData {
    private long pendapatan;
    private long laba;
    private long biaya;
    private int tahun;
    public long getPendapatan() {
        return pendapatan;
    }
    public void setPendapatan(long pendapatan) {
        this.pendapatan = pendapatan;
    }
    public long getLaba() {
        return laba;
    }
    public void setLaba(long laba) {
        this.laba = laba;
    }
    public long getBiaya() {
        return biaya;
    }
    public void setBiaya(long biaya) {
        this.biaya = biaya;
    }
    public int getTahun() {
        return tahun;
    }
    public void setTahun(int tahun) {
        this.tahun = tahun;
    }
    
}
