package org.acme.utils.objectPost;

public class ReviewsData {
    private int total_project;
    private long total_revenue;
    private double ontime_rate;
    private double average_rating;
    public int getTotal_project() {
        return total_project;
    }
    public void setTotal_project(int total_project) {
        this.total_project = total_project;
    }
    public long getTotal_revenue() {
        return total_revenue;
    }
    public void setTotal_revenue(long total_revenue) {
        this.total_revenue = total_revenue;
    }
    public double getOntime_rate() {
        return ontime_rate;
    }
    public void setOntime_rate(double ontime_rate) {
        this.ontime_rate = ontime_rate;
    }
    public double getAverage_rating() {
        return average_rating;
    }
    public void setAverage_rating(double average_rating) {
        this.average_rating = average_rating;
    }

    
}
